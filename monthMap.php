<?php
function getMonth($integer)
{
	if($integer == 1)
		return 'January';
	else if($integer == 2)
		return 'February';
	else if($integer == 3)
		return 'March';
	else if($integer == 4)
		return 'April';
	else if($integer == 5)
		return 'May';
	else if($integer == 6)
		return 'June';
	else if($integer == 7)
		return 'July';
	else if($integer == 8)
		return 'August';
	else if($integer == 9)
		return 'September';
	else if($integer == 10)
		return 'October';
	else if($integer == 11)
		return 'November';
	else if($integer == 12)
		return 'December';	
}
?>